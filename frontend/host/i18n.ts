function humanizeCapabilitySegment(segment: string): string {
  return segment
    .replace(/([a-z0-9])([A-Z])/g, '$1 $2')
    .replace(/[_-]+/g, ' ')
    .replace(/\b\w/g, (match) => match.toUpperCase())
}

export function formatCapabilityLabel(locale: HostLocale, capabilityId: string): string {
  const exact = capabilityLabelOverrides[locale][capabilityId as keyof typeof capabilityLabelOverrides.en]
  if (exact) {
    return exact
  }

  return capabilityId
    .split('.')
    .map((segment) => capabilitySegmentLabels[locale][segment as keyof typeof capabilitySegmentLabels.en] ?? humanizeCapabilitySegment(segment))
    .join(locale === 'zh-CN' ? ' / ' : ' / ')
}
