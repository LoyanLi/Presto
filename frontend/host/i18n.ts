export type HostMessageKey = keyof typeof hostMessages.en

export function getSystemLocaleCandidates(): string[] {
  const navigatorValue =
    globalThis.navigator ??
    (typeof window !== 'undefined' ? window.navigator : undefined)
  if (!navigatorValue) {
    return ['en']
  }

  const languages = Array.isArray(navigatorValue.languages) ? navigatorValue.languages : []
  if (languages.length > 0) {
    return languages
  }

  return typeof navigatorValue.language === 'string' ? [navigatorValue.language] : ['en']
}

export function resolveHostLocale(
  preference: HostLanguagePreference,
  systemLocales: readonly string[] = getSystemLocaleCandidates(),
): HostLocale {
  if (preference === 'en' || preference === 'zh-CN') {
    return preference
  }

  for (const candidate of systemLocales) {
    const normalized = String(candidate || '').toLowerCase()
    if (normalized.startsWith('zh')) {
      return 'zh-CN'
    }
    if (normalized.startsWith('en')) {
      return 'en'
    }
  }

  return 'en'
}

export function translateHost(
  locale: HostLocale,
  key: HostMessageKey,
  params?: Record<string, string | number>,
): string {
  const template = hostMessages[locale][key] ?? hostMessages.en[key] ?? key
  if (!params) {
    return template
  }

  return template.replace(/\{(\w+)\}/g, (_match, name) => String(params[name] ?? ''))
}

