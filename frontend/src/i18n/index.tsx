const MESSAGES: Record<Locale, Record<string, string>> = {
  'en-US': EN_US,
  'zh-CN': ZH_CN,
}

function formatMessage(template: string, vars?: Record<string, string | number>): string {
  if (!vars) return template
  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(vars[key] ?? `{${key}}`))
}

function detectInitialLocale(): Locale {
  if (typeof window === 'undefined') return 'en-US'
  const saved = window.localStorage.getItem(LOCALE_STORAGE_KEY)
  if (saved === 'en-US' || saved === 'zh-CN') {
    return saved
  }
  const lang = window.navigator.language.toLowerCase()
  return lang.startsWith('zh') ? 'zh-CN' : 'en-US'
}

const I18nContext = createContext<I18nContextValue | null>(null)

export function I18nProvider(props: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(() => detectInitialLocale())

  useEffect(() => {
    if (typeof document !== 'undefined') {
      document.documentElement.lang = locale
    }
  }, [locale])

  const setLocale = useCallback((next: Locale) => {
    setLocaleState(next)
    if (typeof window !== 'undefined') {
      window.localStorage.setItem(LOCALE_STORAGE_KEY, next)
      document.documentElement.lang = next
    }
  }, [])

  const t = useCallback(
    (key: string, vars?: Record<string, string | number>) => {
      const primary = MESSAGES[locale][key]
      const fallback = MESSAGES['en-US'][key]
      const raw = primary ?? fallback ?? key
      return formatMessage(raw, vars)
    },
    [locale],
  )

  const value = useMemo<I18nContextValue>(() => ({ locale, setLocale, t }), [locale, setLocale, t])

  return <I18nContext.Provider value={value}>{props.children}</I18nContext.Provider>
}

export function useI18n(): I18nContextValue {
  const ctx = useContext(I18nContext)
  if (!ctx) {
    throw new Error('useI18n must be used within I18nProvider')
  }
  return ctx
}
